    private void tombolRegister(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolRegister
        String name = jTextField1.getText().trim();
        String password = jTextField2.getText().trim();

        if (name.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama dan kata sandi tidak boleh kosong!", "Input Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            Akun regist = new Akun(name, password, "user");
            if(regist.registerUser()) {
                LoginPage loginsan = new LoginPage(hasanUsername, hasanPassword);
                loginsan.setVisible(true);
            } else {
                jTextField1.setText("");
                jTextField2.setText("");
            }
        } catch (HeadlessException e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat mendaftar: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_tombolRegister

