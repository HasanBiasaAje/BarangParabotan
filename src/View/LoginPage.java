    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String name = jTextField1.getText().trim();
        String password = jTextField2.getText().trim();

        if (name.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama dan kata sandi tidak boleh kosong!", "Input Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            Akun login = new Akun(name, password, "");
            login.loginUser(this);
        } catch (HeadlessException e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat mendaftar: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

